export type DictKey = keyof typeof DICT;

// ── Context ──────────────────────────────────────────────────────
interface LangCtx { lang: Lang; toggle: () => void; t: (k: DictKey) => string; }
const Ctx = createContext<LangCtx>({ lang:"en", toggle:()=>{}, t:(k)=>DICT[k].en });

export const useLang = () => useContext(Ctx);

// ── Provider — wrap your App or LandingPage with this ───────────
export function LangProvider({ children }: { children: ReactNode }) {
  const [lang, setLang] = useState<Lang>("en");
  const toggle = () => setLang(l => l === "en" ? "ar" : "en");
  const t = (k: DictKey): string => DICT[k][lang];
  return _jsx(Ctx.Provider, { value:{ lang, toggle, t }, children });
}